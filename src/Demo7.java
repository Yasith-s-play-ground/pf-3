void main() {

    System.out.println("Direct Entry\r Program"); //Carriage Return
    System.out.println("Hello DEP-1200\b\b!"); //Backspace
    System.out.println("Hello\n\nThis is awesome"); //New Line
    System.out.println("Hello\tDirect\tEntry\tProgram"); //Tab Space
    System.out.println("Hello\sDirect\sEntry\sProgram"); //Space
    System.out.println("Hello\fDirect\fEntry\fProgram"); //Form Feed ( Page Break )

    System.out.println("\\"); // Back slash
    System.out.println("\""); //Double quote
    System.out.println("'"); //Single quote in string - no need to escape
    System.out.println('a'); //Character in char - no need to escape
    System.out.println('\''); //Single quote in char

}
